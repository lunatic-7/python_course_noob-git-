# syntax - [start argument : stop argument -1 : step]
print("wasif"[0:5:1]) # here 1 means with no gap
print("wasif"[0:5:2]) # 2 means with gap of 1
print("wasif"[0::2]) # whole word with 1 gap
print("wasif"[::3]) # whole word with 2 gaps
print("wasif"[5::-1]) # starting from fifth word from last
print("wasif"[::-1]) # starting from last word