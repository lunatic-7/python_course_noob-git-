name = "WaSIf nAdEEM"

#1  len() function
print(len(name)) # it counts the no. of characters in our string.

#2  lower() method
print(name.lower()) # it makes all letters small.

#3  upper() method
print(name.upper()) # it makes all letters capital.

#4  title() method
print(name.title()) # it makes 1st letter of each word capital.

#5 count() method
print(name.count("E")) # it counts specific no. of chr. which we ask for. eg. here "E"